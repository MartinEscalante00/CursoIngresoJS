function Mostrar()
{
	//definimos la variable
	var contador;
	//igualamos a contador a = 0
	contador=0;
	// creamos un while, tiene que ser menor a 10

	while(contador<10)
	{
		// contador + 1, para que vaya en uno en uno
		contador ++;
		//br para que vaya en forma de columna
		document.write("<br>"+contador);
	}


}//FIN DE LA FUNCIÃ“N